    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        try {
            String nombre = TfNombre.getText();
            long dpi = Long.parseLong(TfDPI.getText());
            
            
            
            String depa = TfDep.getText();
            String municipio = TfMuni.getText();
            String lugarV = TfLugarV.getText();
            int numDosis = Integer.parseInt(TfNoDosis.getText());
            String vacuna1 = TfDosis1.getText();
            String vacuna2 = TfDosis2.getText();
            String vacuna3 = TfDosis3.getText();
            
            avl.insertarAlArbol(nombre, dpi);
            avl.actualizarDatos(dpi, nombre, depa, municipio, lugarV, numDosis, vacuna1, vacuna2, vacuna3);
            JOptionPane.showMessageDialog(null, "Nodo agregado correctamente");
            this.dispose();
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Por favor, ingrese valores v√°lidos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_agregarActionPerformed

