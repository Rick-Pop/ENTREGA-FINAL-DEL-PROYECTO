    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        long numero;
        
        if (!arbolavl.estaVacio()) {
            try {
                numero = Long.parseLong(BuscarDPI.getText());

                NodoArbolAVL tmp = arbolavl.buscarN(numero, arbolavl.raiz);
                if (tmp == null) {
                    JOptionPane.showMessageDialog(null, "El nodo buscado no se encuentra en el árbol", "Nodo No Encontrado", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    

                    dpiEncontrado = numero;
                    JOptionPane.showMessageDialog(null, "Registro Encontrado");
                    Nombre.setText(tmp.getNombre());
                    DPI.setText(String.valueOf(tmp.getDpi()));
                    Departamento.setText(tmp.getDepa());
                    Municipio.setText(tmp.getMunicipio());
                    Lugar.setText(tmp.getLugVacuna());
                    NumDosis.setText(String.valueOf(tmp.getNumDosis()));
                    Dosis1.setText(tmp.getFVacuna1());
                    Dosis2.setText(tmp.getFVacuna2());
                    Dosis3.setText(tmp.getFVacuna3());

                    

                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Por favor, ingrese un número de DPI válido.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "El Árbol está vacío", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_BuscarActionPerformed

