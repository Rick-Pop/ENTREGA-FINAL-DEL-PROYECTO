    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        try {
            String nombre = Nombre.getText();
            long dpi = Long.parseLong(DPI.getText());
            String departamento = Departamento.getText();
            String municipio = Municipio.getText();
            String lugarVacunacion = Lugar.getText();
            int cantidad = Integer.parseInt(NumDosis.getText());
            String fechaDosis1 = Dosis1.getText();
            String fechaDosis2 = Dosis2.getText();
            String fechaDosis3 = Dosis3.getText();
            
            arbolavl.actualizarDatos(dpi, nombre, departamento, municipio, lugarVacunacion, cantidad, fechaDosis1, fechaDosis2, fechaDosis3, dpiEncontrado);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error");
        }
    }//GEN-LAST:event_GuardarActionPerformed

